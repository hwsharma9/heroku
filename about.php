                        1987 with 2 crore funding from DRDO. Also, M.Tech (Computer
                        Science) programme was started in 1992 approved by AICTE. M.Tech
                        (Network Management & Information Security) and M.Tech (Software
                        Engineering & Information Architecture) was started in 2009
                        approved by AICTE. Keeping in view the social needs in the area of
                        information technology, the department is also running MBA
                        (Computer Management), M.Sc. (Information Technology) and BCA
                        programme on self supported basis. The School is running PhD
                        (Computer Science) programme in a variety of domains.<br /><br />
                    </p>
